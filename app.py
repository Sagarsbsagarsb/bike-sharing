y = torch.from_numpy(y).float()

# Model definition
class ComponentFailureModel(torch.nn.Module):
    def __init__(self):
        super().__init__()
        self.linear = torch.nn.Linear(3, 5)  # 3 inputs -> 5 outputs (one for each component)

    def forward(self, x):
        return torch.sigmoid(self.linear(x))

# Train the model
model = ComponentFailureModel()
optimizer = torch.optim.Adam(model.parameters(), lr=0.01)
loss_fn = torch.nn.BCELoss()
for epoch in range(1000):
    optimizer.zero_grad()
    pred = model(X)
    loss = loss_fn(pred, y)
    loss.backward()
    optimizer.step()

# Prediction function
components = ['brakes', 'tires', 'chain', 'gears', 'electronics']
def predict_component_risks(temp, vib, pressure, duration=45, base_duration=45):
    input_arr = np.array([[temp, vib, pressure]])
    input_arr = (input_arr - mean) / std
    input_tensor = torch.from_numpy(input_arr).float()
    pred = model(input_tensor).detach().numpy()[0]
    risks = dict(zip(components, pred))
    # Scale by duration (simulates cumulative wear; adjust logic as needed)
    scale_factor = duration / base_duration
    for comp in risks:
        if risks[comp] > 0.5:
            risks[comp] = min(1.0, risks[comp] * scale_factor)
    return risks

