labels = first_maintenance[['equipment_id', 'brakes_replaced', 'tires_replaced', 'chain_replaced', 'gears_replaced', 'electronics_replaced']]
for col in ['brakes_replaced', 'tires_replaced', 'chain_replaced', 'gears_replaced', 'electronics_replaced']:
    labels[col] = labels[col].map({'Yes': 1, 'No': 0})
